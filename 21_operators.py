#Arithmetic Operators Discussion

var1 = 15
var2 = 2

#addition
print(var1+var2)

#subtraction
print(var1-var2)

#multiplication
print(var1*var2)

#division
print(var1/var2)

#modulus
print(var1%var2)

#floor division
print(var1//var2)

#power eg: a^b
print(var1**var2)


#Comparison Operators Discussion
                                    #it returns boolean values: True or False

#less than
print(5<10)
print(10<5)

#greater than
print(5>10)
print(10>5)

#less than OR equal
print(5<=10)
print(10<=5)

#greater than OR equal
print(5>=10)
print(10>=5)

#equal
print(5==10)
print(10==10)

#not equal
print(5!=10)
print(5!=5)